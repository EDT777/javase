package 第一阶段.day_02;

public class ComparisonOperatorDemo {

    public static void main(String[] args) {
        int a=123;
        int b=321;
        //（1）使用>、<、>=、<=、!=、==等比较运算符操作两个整数类型的常量
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
    }

}
