package 第一阶段.day_01;

public class Demo {//八大基本数据类型
    public static void main(String[] args) {

        //byte
        System.out.println(127);
        //short
        System.out.println(127);
        //int
        System.out.println(127);
        System.out.println(0B00000001);//二进制
        System.out.println(010);//八进制
        System.out.println(0X10);//十六进制
        //long
        System.out.println(127L);
        //float
        System.out.println(3.14F);
        //double
        System.out.println(3.14);//小数默认类型
        //char
        System.out.println('奋');
        //boolean
        System.out.println(true);//真
        System.out.println(false);//假
        //==============================================
        //String:引用类型
        System.out.println("奋斗的青春");

    }
}
